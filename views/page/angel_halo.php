	<h1>Angel Halo</h1>
	<p><i>Manipulação da luz. É capaz de atacar com laser e criar ilusões.</i></p>
	<p>Esta Síndrome dá a capacidade de manipular o grau de luz e sua taxa de refração,bem como produzir luz.
	Com esta fonte de luz,flashes que cegam podem ser usados e ataques lasers são possíveis quando os comprimentos de onda são combinados.</p>
	<p>Ao usar essas habilidades,sabe-se que os overeds brilham,o que deu a Síndrome o nome de "Angel Halo".</p>
	<h2>Poderes: Angel Halo</h2>
	<p>A síndrome dá ao Overed o controle sobre a luz e a capacidade de melhorar seus sentidos. Um Overred pode usar seus Poderes para lançar poderosos ataques de laser, ou ele pode escolher um estilo de luta mais silencioso e refratar a luz, dando a suas armas ou a si mesmo furtividade.</p>
