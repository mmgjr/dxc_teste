<h1>Balor</h1>
<p><i>Controla a gravidade através do uso de um esfera chamada de "Evil Eye"(olho maligno) ou "Jewel"(gema).
Algumas teorias sugerem que essa esféra seja incrivelmente densa e pode criar um campo magnético ao ser girada.Esses teorias não são comprovados,mas está claro que Balor Overeds podem criar um fenômeno que só pode ser descrito como controle da gravidade.
O tamanho ,quantidade e formado dessa esfera é diferente de Overed para Overed.
Diz-se que quanto mais esferas um Overed pode produzir,maior é o seu poder.
Contudo, essa especulação é apenas um rumor.
O nome Balor foi tirado do Deus celta que possuia o "olho maligno" que governava a morte.
Ele cria poços(áreas) de gravidade para manipular o tempo e o espaço.</i></p>
<h2>Poderes: Balor</h2>
<p>A síndrome concede controle sobre a gravidade. Com esses poderes,um Overed pode distorcer o tempo e o espaço.
Além de esmagar o oponente,Balor também tem a opção de dar penalidades as ações do inimigo ,ou transportar e acelerar os aliados.
Essa capacidade de mudar as táticas é uma das características do Balor.
</p>	


