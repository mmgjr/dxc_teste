	<h1>Síndrome e Poderes</h1>
	<div>
		Como explicado na página 73, Síndromes são as diferentes habilidades que o vírus fornece.
		Os Overed terá de 1 a 3 síndromes e será capaz de usar os Poderes que pertence às suas síndromes.
		
	</div>

	<h2>Poderes Simples</h2>
	<div>
		Os Poderes de um Overed não estão limitados apenas à batalha.Usando uma pequena porção de seus poderes , eles podem realizar pequenas ações espetaculares.
		Esses truques são os Poderes Simples do Overed.
		São poderes que o personagem usa sem ter que aumentar sua taxa de invasão.
		Na maioria dos casos , não exige verificação.No entendo, o GM pode requisitar um teste de verificação se achar necessário.
		Os poderes simples não têm qualquer aplicação em batalha ,mas eles afetão o cenário.
		Assim, o GM pode proibir o uso de certos Poderes Simples a qualquer momento.
	</div>

	<h2>Adquirindo poderes Simples</h2>
	<div>
		Um jogador pode adquirir um Poder Simples de suas síndromes escolhidas gastando 2 pontos de experiência.
		Custa 2 pontos de experiência para aumentar um Poder Simples em 1 nível.
		O Breed(classe) não restringe o tipo de Poderes Simples que ele pode adquirir.
	</div>

	<h2>Sobre restrição de combinações</h2>
	<div>
		Poderes simples não podem ser combinados com qualquer outros Poderes.
	</div>

