<h1>Criação rápida</h1>
<p>Para a criação rápida,os jogadores devem escolher e copiar os dados estatísticos de um dos perosnagens de amostra deste livro e então criar dados pessoais exclusivos.
Este método é recomendado para novos jogadores ou jogadores que desejam começar a sessão imediatamente.</p>
<h2>Exemplos de Recomendações e Seleções de personagens.</h2>
<p>Durante a criação rápida, o GM recomendará certos personagens de amostra para serem usados em um cenário.
Os jogadores devem rever o handout de Trailer e cenário e consultar com seus colegas jogadores antes de selecionar um personagem de amostra.
Consulte o início do capítulo de cenários para mais handouts e trailers.
</p>
<h2>Copiando dados.</h2>
<p>
	Depois que o personagem de amostra for selecionado,copie os dados para a seção apropriada da folha de personagem. 
</p>
<h2>Criando dados pessoais</h2>
<p>Depois que as estatísticas dos pesonagens forem copiadas, vá para página 56 e crie os dados pessoais.
Os dados pessoais representam a história e os relacionamentos de um personagem e serão bse para o histórico e a personalidade de um personagem.
Isso fará com que seu personagem seja único a partir dos personagens de exemplo.</p>
<h2>Escolhendo os dados pessoais.</h2>
<p>Há duas,rolar ou escolher,caso o jogador escolha rolar-ele deve rolar 1d100 ou 2d10 e selecionar o valor do(s) dados na tabela.Caso opte pela escolha, o jogador apenas escolhe os dados na tabela.</p>
