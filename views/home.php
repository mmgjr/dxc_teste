<h1>Bem-vindo ao outro lado!</h1>
<p>
	<i>"Hoje é uma repetição de ontem. Amanhã será uma repetição de hoje. Este ciclo mundano conhecido como vida fez o mundo parecer estagnado. Quem suspeitaria que o mundo tem se distorcido?"</i>
</p>
<p>
	Um vírus chamado Renegade(Renegado) foi liberado sobre a humanidade, transformando os seres humano por dentro.
	O Renegado quebra as leis da natureza e concede poderes incomensuráveis.
	No entendo, esse poderes vem com um preço.
	Em troca dos poderes sobrenaturais,alguém perderá sua mente e alma. Muitos que sucumbiram ao vírus tornaram-se bestas furiosas.Outro, cometeram suicídio ou feriram pessoal inocentes.
	 Mas há um grupo de pessoas que permanecem entre o normal e o anormal, lutando para proteger o mundo. Tanto os humanos e os monstros se referem a eles pelo mesmo título - Double Crossers.
</p>
<p>
	O mundo de Double Cross é muito similar ao nosso. Contudo, há um segredo que se mantém obscuro. O segredo é a existência do Renegado e sua capacidade de transformar as pessoas em Overeds.
	Os Overds são pessoas que despertaram poderes sobrenaturais quando o vírus em seu corpo torna-se ativo.
	Quando um Overed usa seus poderes, ele corre o risco de perder o controle e eventualmente deixará sua humanidade de lado.
	Os pessoas comuns não fazem ideia que o mundo está cheio de Overeds. Ninguém nota que o mundo está sobre um gelo fino, prestes a quebrar.
</p>
	<div>
		<h2>The Renegade/O Renegado</h2>
		<p>É o vírus que se espalhou pelo mundo devido a um certo incidente. <strong>Oitenta por cento</strong> da população foi atingida pelo vírus. Quando a pessoa portadora do vírus experimenta um extremo choque mental ou físico, o Renegade(Renegado) torna-se ativo e possibilita a transformação do portador em Overed.
		</p>	
	</div>
	<div>
		<h2>Overed</h2>
		<p>Overeds são pessoas que ganharam superpoderes após o vírus se tornar ativo.Em geral, qualquer pessoa que tenha superpoderes ,mas não se tornou um Gjaum é referenciado como Overed.Em troca desses poderes, os Overeds devem lidar com os impulsos do vírus ativo.</p>	
	</div>
	<div>
		<h2>Gjaum</h2>
		<p>
			São os Overeds que perderam sua sanidade para o Renegade e a sua "corrupção/invasão".
			Gjaums ,normalmente,não são humanos e não são capazes de se comunicar.Embora, há casos em que um Gjaum mantém sua forma humanóide e apresenta resquícios ou partes de sua sanidade.
		</p>	
	</div>
	<div>
		<h2>Impulses</h2>
		<p>
			Um impulso é um desejo destrutivo que um Overed experimenta quando o vírus se torna extremamente ativo.
			Overeds torna-se um Gjaum quando eles deixam um impulso controlá-los.
			Impulsos ocorrem quando um Overed experimenta um súbito choque físico ou mental.
		</p>	
	</div>
