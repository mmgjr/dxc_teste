		<h2>Universal Guardian Network(UGN)</h2>
		<p>
			Um a Organização mundial cujo objetivo é apoiar a coexistência de Overeds e humanos.
			Usando seus muitos ramos, a UGN lida com incidentes relacionados ao Renegade e manta a existência do vírus em segredo até a hora certa.
		</p>	
	</div>
	<div>
		<h2>False Hearts(FH)</h2>
		<p>
			Uma organização terrorista formada por Overeds e alguns Gjaums que se opoem a UGN.O objetivo deles é revelar a existência do Renegade e criar uma sociedade governada por Overeds.
			Os falsos corações tem agentes em todo o mundo. Embora,esses agentes não são unidos em uma única Organização Cada membro trabalha em seus próprios objetivos.
		</p>	
	</div>
